                        <h1 class="blog-post-title mb-3">Robonesia Sukses Gelar Medan Robotic Competition 2024</h1>
                        <p class="blog-post-meta text-muted">
                            29 Februari 2024 by <a href="#">Robonesia Medan</a>
                        </p>

                        <!-- Paragraf Pendahuluan -->
                        <p>
                            Robonesia Medan berhasil menyelenggarakan <b>Medan Robotic Competition (MRC) 2024</b> dengan sukses di 
                            Taman Budaya Medan pada Sabtu, 24 Februari 2024. Acara ini mengusung tema <em>"Membentuk Generasi Cerdas Teknologi"</em> 
                            yang bertujuan untuk memfasilitasi generasi muda dalam menunjukkan kreativitas dan inovasi mereka di bidang robotika. 
                            Kompetisi ini mendapatkan apresiasi tinggi dari peserta, orang tua, dan guru.
                        </p>

                        <hr>

                        <!-- Paragraf 1 -->
                        <p>
                            Medan Robotic Competition 2024 diikuti oleh 48 peserta dari berbagai sekolah dasar di Kota Medan. Para peserta 
                            bersaing dalam enam kategori lomba, yakni: <strong>Basic Assembly Starter, Basic Animation Starter, Creative Robot, 
                            Basic Animation Mover, Creative Animation,</strong> dan <strong>Soccer Bot</strong>. Setiap kategori dirancang 
                            untuk menguji keterampilan teknis, kreativitas, dan kemampuan analitis peserta dalam merancang serta mengoperasikan robot.
                        </p>

                        <!-- Paragraf 2 -->
                        <p>
                            Dalam sambutannya, Branch Manager Robonesia Medan, Umar Hanif Nasution, mengatakan, "Kompetisi ini menjadi bukti nyata 
                            semangat kita dalam menggali potensi generasi muda di bidang teknologi. Kami berharap kegiatan ini dapat mengembangkan 
                            kreativitas, keterampilan kolaborasi, serta meningkatkan kepercayaan diri para peserta dalam menghadapi tantangan."
                        </p>

                        <!-- Paragraf 3 -->
                        <p>
                            Salah satu orang tua peserta menyampaikan bahwa Medan Robotic Competition adalah kegiatan yang sangat positif bagi anak-anak. 
                            "Selain melatih keterampilan teknis dan motorik mereka, kompetisi ini juga menjadi ajang untuk memperluas wawasan sosial 
                            karena anak-anak dapat bertemu teman-teman baru dari sekolah lain," ujar seorang orang tua peserta.
                        </p>

                        <!-- Paragraf 4 -->
                        <p>
                            Mida, seorang guru pendamping dari salah satu sekolah peserta, menyatakan, "Saya sangat terkesan dengan kemampuan siswa 
                            kelas 1 SD yang sudah bisa merakit robot dengan cepat. Kompetisi ini sangat penting, karena di era sekarang, menguasai 
                            teknologi adalah hal yang sangat diperlukan. Saya harap anak-anak dapat menerapkan keterampilan ini dalam kehidupan sehari-hari."
                        </p>

                        <!-- Paragraf 5 -->
                        <p>
                            Dalam kategori Basic Assembly Starter, juara pertama diraih oleh Aditya Rifki Hamiza dari Sekolah Al-Azhar Asy Syarif Sumut, 
                            diikuti oleh Faatir Faturrahman (Yayasan Pendidikan Shafiyyatul Amaliyyah) di posisi kedua, dan Muhammad Arkhan Al Habsyi 
                            (Sekolah Azzakiyah Islamic Leadership) sebagai juara ketiga. Pada kategori Basic Animation Starter, juara pertama diraih 
                            oleh Qiara Cinta Twinsani, diikuti oleh Alifia Anggita Maharani Hsb dan Rayyandra Ghaisan Al-Fatih sebagai juara kedua dan ketiga.
                        </p>

                        <!-- Paragraf 6 -->
                        <p>
                            Salah satu kategori paling seru adalah Soccer Bot, di mana peserta harus memprogram robot untuk bermain sepak bola. 
                            Kategori ini dimenangkan oleh Syahid Zul Akrami Siregar dari SD Alwaliyah 09 sebagai juara pertama, Girban Al Faqih (Sekolah Al-Azhar 
                            Asy Syarif Sumut) sebagai juara kedua, dan Alfa Rizky Rahman Tanjung dari sekolah yang sama di posisi ketiga.
                        </p>

                        <!-- Paragraf 7 -->
                        <p>
                            Berdasarkan jumlah medali yang diraih, Sekolah Al-Azhar Asy Syarif Sumut berhasil keluar sebagai juara umum Medan Robotic Competition 2024. 
                            Prestasi ini menjadi kebanggaan bagi pihak sekolah, siswa, dan orang tua, sekaligus menegaskan pentingnya pendidikan teknologi sejak dini.
                        </p>
